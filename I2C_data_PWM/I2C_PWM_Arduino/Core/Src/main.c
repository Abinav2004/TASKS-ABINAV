void HAL_I2C_AddrCallback(I2C_HandleTypeDef *hi2c, uint8_t TransferDirection, uint16_t AddrMatchCode)
//function called when slave address matches with master requirement
{
	if(TransferDirection == I2C_DIRECTION_TRANSMIT)
//transaction direction decides whether the master requested data or sent data to slave
	{
		HAL_I2C_Slave_Seq_Receive_IT(hi2c,Recieved_Data,size,I2C_FIRST_AND_LAST_FRAME);
		//recieve data from master and interrupt flag raised once done
	}
	else{
		Error_Handler();
	}
}
uint16_t CCR_Calculation(uint8_t IP)
{
	Duty_Period = ((float)IP/100)*5000;// counter period is set to 5000
	return Duty_Period;
}

